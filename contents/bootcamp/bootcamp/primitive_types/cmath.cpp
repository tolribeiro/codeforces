#include <iostream>
#include <cmath>

using namespace std;

int main () {
    cout << abs(-34) << endl;
    cout << fabs(-3.14) << endl;
    cout << ceil(2.17) << endl;
    cout << floor(3.14) << endl;
    cout << min(10, -4) << endl;
    cout << max(3.14, 15.0) << endl;
    cout << pow(2.71, 3.14) << endl;
    cout << log(7.12) << endl;
    cout << sqrt(225) << endl;
    
    return 0;
}
